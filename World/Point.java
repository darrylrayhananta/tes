package World;

public class Point {
    private Integer x;
    private Integer y;

    // Konstruktor
    public Point(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    // Getter untuk x
    public Integer getX() {
        return x;
    }

    // Getter untuk y
    public Integer getY() {
        return y;
    }

    // Setter untuk x
    public void setX(Integer x) {
        this.x = x;
    }

    // Setter untuk y
    public void setY(Integer y) {
        this.y = y;
    }
}
